// Deklarerer en variabel med en string
let firstname = 'Hans';
console.log(typeof firstname);

// Deklarerer en variabel med et heltal (number)
let age = 23
console.log(typeof age);

// Deklarerer en variabel med et decimaltal (number)
let price = 99.95
console.log(typeof price);

// Deklarerer en variabel med en boolean (true/false)
let result = true
console.log(typeof result);

// Deklarerer en variabel med et array (indekseret variabel)
let cars = [
	'Ford', 'Mercedes', 'Fiat', 'Opel'
]
console.log(cars); // typeof vil retunere objekt 

// Deklarerer en variabel med et objekt (properties & methods)
let car = {
	brand: 'Audi',
	model: 'A4',
	doors: 5,
	start: () => {},
	break: () => {}
}
console.log(car);

